//Basic operator functions
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}


console.log(add(2, 2)) // 4
console.log(subtract(5, 3)) // 2
console.log(multiply(2, 5)) // 10
console.log(divide(6, 3)) // 2
